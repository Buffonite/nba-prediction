# ── Optional feature toggles ─────────────────────────────────────────────────
USE_ELO_FEATURES     = True   # add ELO ratings (recommended — strong signal)
USE_INJURY_FEATURES  = True   # add star availability (slower, extra API calls)

# ── ELO rating system parameters ──────────────────────────────────────────────
# v2 update: lowered K-factor and raised season carryover to make ratings
# less reactive to short hot streaks while preserving elite teams' true
# skill across seasons. This addresses two top items in MODEL_LIMITATIONS.md:
#   - K=20 → 12 reduces "recency bias from rolling features"
#   - carryover 0.75 → 0.88 prevents elite teams from being undervalued
#     coming into each new season
ELO_INITIAL          = 1500.0
ELO_K_FACTOR         = 12.0
ELO_HOME_ADVANTAGE   = 100.0
ELO_SEASON_CARRYOVER = 0.88

# ── Injury / star-availability parameters ─────────────────────────────────────
TOP_N_STARS          = 5        # number of "star" players counted per team

# ── Betting odds (optional) ───────────────────────────────────────────────────
# Real bookmaker odds are one of the strongest single-game predictors.
# Off by default since real historical odds require a paid data source.
USE_ODDS_FEATURES    = False
ODDS_CSV_PATH        = "data/raw/odds.csv"   # path to historical odds CSV
ODDS_API_KEY         = ""                    # https://the-odds-api.com (free 500/mo)

# ── v2 improvements (toggle on/off) ───────────────────────────────────────────
# Strength-of-schedule weighting in rolling features: each past game's
# contribution is weighted by the opponent's pre-game ELO. Beating a
# 1700-ELO team counts more than beating a 1400-ELO team.
USE_SOS_WEIGHTED_FEATURES = True

# Temperature scaling: fit a single scalar on validation to correct over-confident
# probabilities. Reduces the model's tendency to output extreme values like 87%.
USE_TEMPERATURE_SCALING   = True

# XGBoost ensemble: train an XGBoost model alongside the NN and average their
# predictions. Tree models typically outperform NNs on tabular data with
# moderate sample sizes.
USE_XGBOOST_ENSEMBLE      = True
ENSEMBLE_NN_WEIGHT        = 0.25   # 0.25 * NN + 0.75 * XGB
# Tree models historically dominate tabular sports data — they handle
# non-linear thresholds (e.g. "ELO diff > 100 → big jump") better than
# small dense networks. Heavier XGBoost weight also damps the NN's
# tendency to over-react to recent hot streaks.

